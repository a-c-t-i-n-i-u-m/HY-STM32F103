;******************************************************************************
;
;                               STARTUP CODE
;                                
;                             KEIL's uVision3 
;                   (RealView Microprocessor Developer Kit)
;
; Filename      : init.s
;******************************************************************************

                PRESERVE8
                AREA   INIT, CODE, READONLY                     ; Name this block of code                                   ;
                THUMB

;******************************************************************************
;                                  IMPORTS
;******************************************************************************
                                
                IMPORT  __main

;******************************************************************************
;                                  EXPORTS
;******************************************************************************

                EXPORT  ResetHndlr

;******************************************************************************
;                                DEFINITIONS
;******************************************************************************


;******************************************************************************
;                             PERFORM ROM/RAM REMAPPING
;******************************************************************************                

                ENTRY
ResetHndlr      

;******************************************************************************
;                              SETUP STACK POINTERS
;******************************************************************************
                

;******************************************************************************
;                                   MOVE TO MAIN
;******************************************************************************
                ldr     r0, =__main
                bx      r0                             	    ; Save this in register for possible long jump              ;

                ALIGN
                END 
        
